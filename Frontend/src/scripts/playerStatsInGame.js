class PlayerData {
    constructor(playerData) {
        this.playerData = playerData;
    }

    // Getter methods for PlayerData

    get gameDuration() {
        return this.playerData.challenges.gameLength
    }

    get assists() {
        return this.playerData.assists;
    }

    get kills() {
        return this.playerData.kills;
    }

    get deaths() {
        return this.playerData.deaths;
    }

    get KDAratio(){
        return this.playerData.challenges.kda.toFixed(2)
    }

    get KDAstring(){
        return this.kills + " / "  + this.assists + " / " + this.deaths 
    }

    get killParticipationPerCent(){
        
        let prcVal = this.playerData.challenges.killParticipation * 100

        return prcVal.toFixed(0)
    }

    get champLevel() {
        return this.playerData.champLevel;
    }

    get championName() {
        return this.playerData.championName;
    }

    get win() {
        return this.playerData.win;
    }

    get items() {
        const { item0, item1, item2, item3, item4, item5, item6 } = this.playerData;
        return [item0, item1, item2, item3, item4, item5, item6]
    }

    get totalMinionsKilled() {
        return this.playerData.totalMinionsKilled
    }

    get neutralMinionsKilled(){
        return this.playerData.neutralMinionsKilled
    }

    get CS() {
        return this.neutralMinionsKilled + this.totalMinionsKilled
    }

    get CSmin(){
        let CSmin = this.CS / (this.gameDuration/60)
        return CSmin.toFixed(1)
    }

    get totalDamageDealtToChampions(){
        return this.playerData.totalDamageDealtToChampions
    }

    get damagePerMinute(){
        return this.playerData.challenges.damagePerMinute.toFixed(0)
    }

    get summoner1Id() {
        return this.playerData.summoner1Id
    }

    get summoner2Id() {
        return this.playerData.summoner2Id
    }

    get mainRune() {
        return this.playerData.perks.styles[0].selections[0].perk
    }

    get secondaryRune(){
        return this.playerData.perks.styles[1].style
    }

    get goldEarned(){
        return this.playerData.goldEarned
    }

    get goldPerMinute(){
        return Math.round(this.playerData.challenges.goldPerMinute)
    }

    get visionScore(){
        return this.playerData.visionScore
    }

    get visionScorePerMinute(){
        return this.playerData.challenges.visionScorePerMinute.toFixed(2)
    }
}




